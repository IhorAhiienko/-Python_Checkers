# При невозможности выигрыша любого из участников игры, партия считается законченной в ничью.
# Правила:
# В игре принимают участие 2 игрока. Игроки располагаются на противоположных сторонах доски.
# Игровое поле (доска) располагается таким образом, что бы угловая темная клетка была расположена  с левой стороны игрока.
# Выбор цвета игроками определяется жребием или по договоренности. Шашки расставляются на трех, ближних к игроку, рядах на темных клетках.
# Право первого хода обычно принадлежит игроку, который играет черными (темными) шашками. Ходы осуществляются соперниками поочередно.
# В начале игры все шашки соперников являются простыми. Простые шашки можно перемещать  только вперед по диагоналям на соседнюю свободную клетку.
# Если простая шашка дошла до последней горизонтали, она становится «дамкой» и обозначается переворачиванием.
# Дамка может ходить на одно поле по диагонали вперёд или назад
# Ход считается сделанным, если участник игры после перемещения шашки отпустил руку.
# Если игрок дотронулся до шашки, он обязан ей сделать ход. Если, кто-либо из соперников хочет поправить шашки, обязан предупредить заранее.
# Взятие шашки соперника производится, переносом через неё своей, в том случае, если она находится на соседней с простой шашкой диагональной клетке и за ней имеется свободное поле.
# Взятие шашки соперника простой шашкой может производиться только вперед. Дамка, при взятии ходит только через одно поле в любую сторону, а не на любое поле диагонали, как в русских или международных шашках.
# Взятие шашки соперника обязательное, но при наличии нескольких продолжений «боя», выбирается любой, наиболее тактически целесообразный (главный критерий – отсутствие дальнейших продолжений для взятий).
